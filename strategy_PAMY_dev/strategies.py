    def _is_in_trade_hours(self, dt, trade_hours):
        """检查时间是否在交易时段内"""
        if not trade_hours:
            return False
        t = dt.time() if hasattr(dt, 'time') else dt
        total_minutes = t.hour * 60 + t.minute
        for session in trade_hours:
            if isinstance(session, str) and '-' in session:
                start_str, end_str = session.split('-')
                start_h, start_m = map(int, start_str.split(':'))
                end_h, end_m = map(int, end_str.split(':'))
                start_min = start_h * 60 + start_m
                end_min = end_h * 60 + end_m
                if start_min <= end_min:
                    if start_min <= total_minutes <= end_min:
                        return True
                else:
                    # 跨午夜
                    if total_minutes >= start_min or total_minutes <= end_min:
                        return True
        return False

    def _align_and_check(self, data_dict, time_recently):
        """检查数据一致性，以主合约 datetime 为基准 reindex 对齐。
        不允许剔除策略，其他品种按主合约时间轴前向填充对齐。
        对于不在对方品种交易时段内的索引，清除前向填充的值，
        避免无夜盘品种（如 LH）在夜盘被错误填充旧数据，导致交叉因子异常。"""
        main = self.main_symbol
        others = self.other_symbols
        keys_to_check = [f'{main}_min_data_concat'] + [f'{s}_min_data_concat' for s in others]
        # 检查空数据
        for k in keys_to_check:
            df = data_dict.get(k)
            if df is None or df.empty:
                print(f"[{self.main_symbol}] {k} 为空")
                return False

        main_key = f'{main}_min_data_concat'
        main_df = data_dict[main_key]

        # 以主合约的 datetime 为基准索引
        if 'datetime' not in main_df.columns:
            return True
        main_index = pd.to_datetime(main_df['datetime']).drop_duplicates()

        # 其他品种按主合约时间轴 reindex 对齐（前向填充，避免错位）
        for k in keys_to_check:
            if k == main_key:
                continue
            df = data_dict[k]
            if 'datetime' not in df.columns:
                continue
            # 设 datetime 为索引，reindex 到主合约时间轴
            df_idx = df.set_index('datetime')
            df_aligned = df_idx.reindex(main_index, method='ffill')

            # 对于不在该品种交易时段内的行，直接删除（而非仅清为NaN），
            # 否则后续 pct_change 等函数的默认 fill_method='pad' 会再次ffill，
            # 导致无夜盘品种（如LH）在夜盘被错误填充旧数据。
            sym = k.replace('_min_data_concat', '')
            trade_hours = self.config.get('symbol_specs', {}).get(sym, {}).get('trade_hours', [])
            if trade_hours:
                mask = ~df_aligned.index.map(lambda dt: self._is_in_trade_hours(dt, trade_hours))
                df_aligned = df_aligned[~mask]

            # 重置索引，datetime 变回列
            df_aligned = df_aligned.reset_index()
            data_dict[k] = df_aligned

        return True

    def _save_min_data(self, data_dict, time_recently, readable_time):
        """保存当前分钟的分钟数据（仅最新一行），并附带 trigger 时点、tick 最后时点、5秒后均价"""
        ts_str = pd.to_datetime(time_recently).replace(second=0).strftime("%Y-%m-%d_%H-%M-00")
        data_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "save_files", self.main_symbol, "data")
        os.makedirs(data_dir, exist_ok=True)

        main = self.main_symbol

        # 1) 取当前分钟的分钟数据（仅一行，不要整个历史）
        min_df = data_dict.get(f'{main}_min_data_concat', pd.DataFrame())
        if min_df is None or min_df.empty:
            return

        current_minute = pd.to_datetime(time_recently).replace(second=0, microsecond=0)
        next_minute = current_minute + pd.Timedelta(minutes=1)

        if 'datetime' in min_df.columns:
            mask = (pd.to_datetime(min_df['datetime']) >= current_minute) & (pd.to_datetime(min_df['datetime']) < next_minute)
            current_min = min_df[mask].copy()
        else:
            current_min = min_df.loc[(min_df.index >= current_minute) & (min_df.index < next_minute)].copy()

        if current_min.empty:
            return

        # 2) 从 tick 数据计算 TWAP（排除前5秒，即 second > 5 的 last_price 均值）
        # 注意：分钟线是右端点（ts=ceil('1min')），current_minute 这根分钟线对应的数据区间是上一分钟
        tick_minute = current_minute - pd.Timedelta(minutes=1)
        tick_next_minute = current_minute

        tick_df = data_dict.get(f'{main}_tick', pd.DataFrame())
        if tick_df is not None and not tick_df.empty and 'datetime' in tick_df.columns:
            tick_df['datetime'] = pd.to_datetime(tick_df['datetime'])
            tick_mask = (tick_df['datetime'] >= tick_minute) & (tick_df['datetime'] < tick_next_minute)
            current_tick = tick_df[tick_mask]

            if not current_tick.empty:
                # trigger 时点
                current_min['trigger_time'] = readable_time
                # tick 数据最后一个时点
                current_min['last_tick_time'] = current_tick['datetime'].iloc[-1]

                # 计算 second 列（支持微秒精度）
                current_tick = current_tick.copy()
                current_tick['second'] = current_tick['datetime'].dt.second + current_tick['datetime'].dt.microsecond / 1e6
                post_sec5 = current_tick[current_tick['second'] > 5]

                if len(post_sec5) > 9:
                    # 排除前5秒的 tick 足够多，直接计算均值
                    twap = post_sec5['last_price'].mean()
                elif len(current_tick) > 9:
                    # 排除前5秒的 tick 太少，但整分钟 tick 够多，退化为整分钟均值
                    twap = current_tick['last_price'].mean()
                else:
                    # tick 太少，记为 NaN
                    twap = np.nan

                current_min['twap'] = twap
                current_min['avg_price_from_5s'] = twap

        fpath = f"{data_dir}/{main}_min_{ts_str}.csv"
        try:
            current_min.to_csv(fpath, index=False)
        except Exception as e:
            print(f"[{main}] 保存分钟数据出错: {e}")

    def _save_results(self, df, fac_df, time_recently, readable_time, calc_time, signal):
        save_path = self.config_loaded['save_path']
        ts_str = pd.to_datetime(time_recently).replace(second=0).strftime("%Y-%m-%d_%H-%M-00")

        # 按子目录保存
        for subdir in ['factors', 'predictions', 'json']:
            os.makedirs(f"{save_path}/{subdir}", exist_ok=True)

        try:
            pred_df_out = df.iloc[-10:].copy()
            pred_df_out['signal'] = signal
            pred_df_out.to_csv(f"{save_path}/predictions/predictions_{ts_str}.csv")
            fac_df.iloc[-10:].to_csv(f"{save_path}/factors/factors_{ts_str}.csv")
            hist = df.iloc[:-1]['weighted_s'].dropna()
            cur = df.iloc[-1]['weighted_s']
            percent_fac = round((hist < cur).mean() * 100, 4) if len(hist) > 0 else 50.0
            status = {
                'timestamp': readable_time,
                'time_recently': str(time_recently),
                'now_pos': self.now_pos,
                'now_holding': self.now_holding,
                'fac_now': cur,
                'current_quantile(%)': percent_fac,
                'factor_calculation_time': calc_time,
            }
            with open(f"{save_path}/json/trading_status_{ts_str}.json", 'w', encoding='utf-8') as f:
                json.dump(status, f, indent=2, ensure_ascii=False)
        except Exception as e:
            print(f"[{self.main_symbol}] 保存结果出错: {e}")


