import bisect

# 全局预缓存交易日列表（只计算一次，使用 chinese_calendar）
_TRADING_DAYS = None

def _init_trading_days():
    """预加载所有交易日，避免每次重复计算"""
    global _TRADING_DAYS
    if _TRADING_DAYS is None:
        import pandas as pd
        all_weekdays = pd.date_range('2021-01-01', '2026-12-31', freq='B').date
        _TRADING_DAYS = sorted([
            d for d in all_weekdays if calendar.is_workday(d)
        ])
    return _TRADING_DAYS


def get_trading_day(today, exclude_days=None):
    """获取下一个交易日（使用 chinese_calendar + 二分查找）"""
    trading_days = _init_trading_days()
    idx = bisect.bisect_right(trading_days, today)
    if idx < len(trading_days):
        return trading_days[idx]
    return None


def datetime_to_trade_date(dt):
    """
    datetime / Timestamp → trade_date
    规则与 sql_writer_dce.py 一致：
      - hour >= 20（夜盘）归入下一交易日（由 chinese_calendar 判定）
      - hour < 20（日盘）归入当日
    """
    if isinstance(dt, pd.Timestamp):
        dt = dt.to_pydatetime()
    if dt.hour >= 20:
        td = get_trading_day(dt.date())
        return td.strftime('%Y-%m-%d') if td else dt.strftime('%Y-%m-%d')
    return dt.strftime('%Y-%m-%d')


