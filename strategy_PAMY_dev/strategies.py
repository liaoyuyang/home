    def _load_fac_cache(self) -> bool:
        """从factor_cache/{trade_date}/{symbol}_fac_{trade_date}.parquet加载历史因子表"""
        pattern = os.path.join(self.cache_dir, "*", f"{self.main_symbol}_fac_*.parquet")
        files = sorted(glob.glob(pattern))
        if not files:
            return False

        dfs = []
        for f in files:
            try:
                df = pd.read_parquet(f)
                if not df.empty:
                    dfs.append(df)
            except Exception as e:
                print(f"[{self.main_symbol}] 加载缓存失败 {os.path.basename(f)}: {e}")

        if not dfs:
            return False

        self.fac_df = pd.concat(dfs)
        self.fac_df = self.fac_df[~self.fac_df.index.duplicated(keep='last')]
        self.fac_df.sort_index(inplace=True)

        # 保留最近N个交易日，删除旧缓存
        keep_days = self.config.get('calculation_params', {}).get('cache_keep_days', 5)
        all_trade_dates = sorted(set(self.fac_df.index.map(datetime_to_trade_date)))
        if len(all_trade_dates) > keep_days:
            keep_dates = set(all_trade_dates[-keep_days:])
            mask = self.fac_df.index.map(datetime_to_trade_date).isin(keep_dates)
            self.fac_df = self.fac_df[mask]
            for td in all_trade_dates[:-keep_days]:
                old_path = os.path.join(self.cache_dir, td, f"{self.main_symbol}_fac_{td}.parquet")
                if os.path.exists(old_path):
                    os.remove(old_path)

        # 从加载的因子表重新预测
        pred_df = pd.DataFrame(
            [self.model_lst[i].predict(self.fac_df) for i in range(len(self.model_lst))],
            columns=self.fac_df.index,
            index=[f'model_{i+1}' for i in range(len(self.model_lst))]
        ).T
        pred_df['weighted'] = pred_df.mul(self.weight_lst, axis=1).sum(axis=1) / sum(self.weight_lst)
        self.pred_df = pred_df

        print(f"[{self.main_symbol}] ✅ 缓存加载 | fac_df={self.fac_df.shape} | pred_df={self.pred_df.shape} | 交易日={all_trade_dates}")
        return True

    def _save_fac_cache(self):
        """按交易日拆分保存fac_df到parquet"""
        if self.fac_df is None or self.fac_df.empty:
            return

        df = self.fac_df.copy()
        df['__td'] = df.index.map(datetime_to_trade_date)
        for td, group in df.groupby('__td'):
            td_dir = os.path.join(self.cache_dir, td)
            os.makedirs(td_dir, exist_ok=True)
            path = os.path.join(td_dir, f"{self.main_symbol}_fac_{td}.parquet")
            group.drop(columns='__td').to_parquet(path)
        # print(f"[{self.main_symbol}] 💾 因子缓存已保存 | 共{df['__td'].nunique()}个交易日 → {self.cache_dir}")

